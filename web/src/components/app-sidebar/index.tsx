export function AppSidebar({ ...props }: React.ComponentProps<typeof Sidebar>) {
  const { setOpenMobile } = useSidebar();
  const { data: accounts, isPending } = useQuery(getAccountQueryOption);

  useEffect(() => {
    if (accounts && accounts.length === 0) setOpenMobile(true);
  }, [accounts, setOpenMobile]);

  if (isPending) {
    return;
  }

  return (
    <Sidebar variant="inset" {...props}>
      <SidebarHeader>
        <AccountSwitcher />
      </SidebarHeader>

      <SidebarContent>
        <NavMain items={data.navMain} />
      </SidebarContent>

      <SidebarFooter>
        <NavUser user={data.user} />
      </SidebarFooter>
    </Sidebar>
  );
}
